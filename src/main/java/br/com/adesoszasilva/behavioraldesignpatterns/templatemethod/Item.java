package br.com.adesoszasilva.behavioraldesignpatterns.templatemethod;

public class Item {
	
	private String name;
	private Double valor;
	
	public Item(String name, Double valor) {
		this.name = name;
		this.valor = valor;
	}

	public String getName() {
		return name;
	}

	public Double getValor() {
		return valor;
	}
	
	
	
	

}
